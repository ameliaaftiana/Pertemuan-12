# Operator Union '|' gabungan dari 2 set
print ('UNION')
merek_hp = {'Samsung', 'Apple', 'Xiaomi', 'Sony'}
merek_ac = {'LG', 'Samsung', 'Panasonic', 'Daikin', 'Sony'}
gabungan = merek_hp | merek_ac
print (gabungan)

# Operator Intersection '&' yang menjadi bagian dari kedua set jadi ada di kedua set
print ('INTERSECTION')
renang = {'siti', 'mail', 'ikhsan', 'upin', 'ipin'}
tenis = {'joko', 'mail', 'ipin', 'upin', 'tejo'}
renang_tenis = renang & tenis # suka renang dan tenis
print(renang_tenis)

